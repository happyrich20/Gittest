package vo;

public class CartVO {
	private int dogId;
	private String dogKind;
	private int dogPrice;
	private String dogImage;
	private int dogQty;
	public int getDogId() {
		return dogId;
	}
	public void setDogId(int dogId) {
		this.dogId = dogId;
	}
	public String getDogKind() {
		return dogKind;
	}
	public void setDogKind(String dogKind) {
		this.dogKind = dogKind;
	}
	public int getDogPrice() {
		return dogPrice;
	}
	public void setDogPrice(int dogPrice) {
		this.dogPrice = dogPrice;
	}
	public String getDogImage() {
		return dogImage;
	}
	public void setDogImage(String dogImage) {
		this.dogImage = dogImage;
	}
	public int getDogQty() {
		return dogQty;
	}
	public void setDogQty(int dogQty) {
		this.dogQty = dogQty;
	}
	
}
